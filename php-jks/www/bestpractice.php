<h2>Consider further measures like:</h2>
<div class="txt">
  <ul>
    <li>Never ever click on links in emails. We don't even sent you any but the notifications about new messages, and these mails never include links.</li>
    <li>Don't tell anybody about your message (Yes, trivial. Just do it...).</li>
    <li>Handle your key-file(s) with great care.</li>
    <li>Don't use company-owned hardware.</li>
    <li>Use a network access not related to you, but still trustworthy.</li>
    <li>Use <a href="paranoia.php">ExtraSecure</a> to add an additional, independent AES-Encryption-Layer for a session.</li>
    <li>Use a non-permanent operating system e.g. <a href="https://tails.boum.org" target="_blank">Tails</a>, which you can boot from USB.</li>
    <li>Use a dedicated device (we are planning a related project... coming soon).</li>
  </ul>
  <b>As a rule of thumb: Take care of your generated key-file, browser, device and internet access point, we take care of the rest.</b>
